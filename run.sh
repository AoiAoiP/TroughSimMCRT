#!/bin/bash

# 设置：如果任何一条命令执行失败（报错），脚本立刻停止运行
set -e

echo "========================================"
echo "🚀 1. 正在编译 CUDA MCRT 引擎..."
echo "========================================"
cd build
# 使用 -j 自动获取 CPU 核心数进行多线程并发编译，速度起飞
make -j$(nproc)

echo ""
echo "========================================"
echo "☀️  2. 正在执行能流仿真追踪..."
echo "========================================"
# 执行仿真，生成的 flux_map.csv 会保存在当前 build 目录下
./mcrt_sim

echo ""
echo "========================================"
echo "📊 3. 正在生成 2D/3D 能流密度图..."
echo "========================================"
# 由于此时我们在 build 目录下，直接调用上层目录的 python 脚本
# 并且脚本读取当前目录的 flux_map.csv 是完美匹配的
python ../scripts/paint_energy_distribution.py

# 跑完后退回根目录
cd ..

echo ""
echo "========================================"
echo "🎉 全流程执行完毕！请查看 build 目录下的图片。"
echo "========================================"